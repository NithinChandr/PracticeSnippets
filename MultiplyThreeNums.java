public class MultiplyThreeNums {

    public static int numbers(int a, int b){
        return a - b;
    }

    public static int nums(int a, int b, int c){
        return a * b * c;
    }


    public static void main(String[] args) {
        System.out.println(numbers(7,4));
        System.out.println(nums(5,2,1));

    }
}
